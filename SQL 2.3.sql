SELECT CUSTOMER.CUST_ID, CUST_NAME, COUNT(ITEM_ID) AS NO_OF_ITEMS, BILL_DATE FROM SALE, CUSTOMER
WHERE SALE.CUST_ID = CUSTOMER.CUST_ID GROUP BY BILL_DATE;

